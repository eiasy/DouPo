package mmo.module.gm.bean;

public class UIData {
	protected int    id;
	protected String name;

	public int getId() {
		return id;
	}

	public void setId(int gameId) {
		this.id = gameId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCompareValue() {
		return id;
	}
}
